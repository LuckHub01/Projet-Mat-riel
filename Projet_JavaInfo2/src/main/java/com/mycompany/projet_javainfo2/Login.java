    private void connecterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connecterBtnActionPerformed
        // TODO add your handling code here:
        
        String un= username.getText();
        String pass= password.getText();
        
        try{
            
            String sql= "SELECT * FROM users where username=? AND password=?";
            ps=con.prepareCall(sql);
            ps.setString(1, un);
            ps.setString(2, pass);
            
            rs=ps.executeQuery();
            
            
            if(rs.next()){
                JOptionPane.showMessageDialog(connecterBtn, "Patientez un instant...");
                
               // Supposons que le JFrame qui doit contenir Home est appel√© HomeFrame
                //Accueil homeFrame = new Accueil();
                 new Accueil(con,un).setVisible(true);
              
                //accueil.getCorpsSlide().init(homePanel);
               
             
            
               
                // Fermer le JFrame actuel
                JFrame currentFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
                if (currentFrame != null) {
                    currentFrame.dispose();
                }

                
              
            }else{
                
                  JOptionPane.showMessageDialog(connecterBtn, "Nom d'utilisateur ou mot de passe incorrects");
              
            }
            
        
        
        }catch(Exception e){
            
            System.err.println("Erreur lors de la tentative de connexion : " + e.getMessage());

            JOptionPane.showMessageDialog(this, "Erreur lors de la connexion: " + e.getMessage());

        }
        
        
    }//GEN-LAST:event_connecterBtnActionPerformed


